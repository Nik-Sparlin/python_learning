from math import *
# this grabs more math functions; must be at top of file

# combine strings and integers
my_num = 12
print("My favorite number is " + str(my_num) + ".")

# common functions in python
# absolute value
new_num = -12
print(abs(new_num))

# exponents - second number is exponent value
print(pow(2, 3))

# return the larger of the two numbers
print(max(4, 6))

# and the smaller of two numbers
print(min(4, 6))

# round numbers
print(round(3.2))
print(round(3.7))

# floor rounds the number down no matter what
print(floor(3.8))

# ceil rounds the number up no matter what
print(ceil(3.2))

# square root
print(sqrt(16))
