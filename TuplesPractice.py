# tuples are data structures similar to a list with some differences
# they are immutable, meaning they cannot be changed/edited -  you create it and you have it
# they can be used for data that is never going to change (like coordinates)
# you can create a list of tuples [(4, 5), (6, 7), (80, 34)] but they still cannot be changed

coordinates = (4, 5)
print(coordinates[0])
print(coordinates[1])

